"use strict";

//------------------------------------------------------------------------------
// Requirements
//------------------------------------------------------------------------------

const assert = require("node:assert");
const fs = require("node:fs");
const path = require("node:path");

//------------------------------------------------------------------------------
// Helpers
//------------------------------------------------------------------------------

const GENERATED_FILE = path.resolve(
	__dirname,
	"../../lib/shared/combining-ranges.generated.js",
);

const GENERATOR_FILE = path.resolve(
	__dirname,
	"../../tools/generate-combining-ranges.js",
);

/**
 * Checks whether a code point falls within any of the given ranges.
 * @param {number} cp The code point to check.
 * @param {[number, number][]} ranges The ranges to check against.
 * @returns {boolean} True if the code point is in any range.
 */
function isInRanges(cp, ranges) {
	return ranges.some(([start, end]) => cp >= start && cp <= end);
}

/**
 * Detects whether the current Node.js build has full ICU support.
 * @returns {boolean} True if `\p{M}` regex works (full ICU available).
 */
function detectFullICU() {
	try {
		/\p{M}/u.test("\u0300");
		return true;
	} catch {
		return false;
	}
}

const HAS_FULL_ICU = detectFullICU();

/**
 * Formats a code point as U+XXXX.
 * @param {number} cp The code point.
 * @returns {string} The formatted string.
 */
function formatCodePoint(cp) {
	return `U+${cp.toString(16).toUpperCase().padStart(4, "0")}`;
}

//------------------------------------------------------------------------------
// Tests
//------------------------------------------------------------------------------

describe("generate-combining-ranges", () => {
	describe("generator script", () => {
		it("should exist", () => {
			assert.ok(
				fs.existsSync(GENERATOR_FILE),
				"tools/generate-combining-ranges.js should exist",
			);
		});
	});

	describe("generated file structure", () => {
		let fileContent;
		let ranges;

		before(() => {
			fileContent = fs.readFileSync(GENERATED_FILE, "utf8");
			ranges = require(GENERATED_FILE);
		});

		it("should have the autogenerated warning header", () => {
			assert.ok(
				fileContent.includes("WARNING: This file is autogenerated"),
				"Generated file should contain autogenerated warning",
			);
			assert.ok(
				fileContent.includes("tools/generate-combining-ranges.js"),
				"Generated file should reference the generator script",
			);
		});

		it("should reference the Unicode data source", () => {
			assert.ok(
				fileContent.includes("DerivedGeneralCategory.txt"),
				"Generated file should reference DerivedGeneralCategory.txt",
			);
		});

		it("should document the Unicode categories covered", () => {
			assert.ok(
				fileContent.includes("Mn") &&
					fileContent.includes("Mc") &&
					fileContent.includes("Me"),
				"Generated file should document Mn, Mc, Me categories",
			);
		});

		it("should export a frozen array", () => {
			assert.ok(Array.isArray(ranges), "Should export an array");
			assert.ok(Object.isFrozen(ranges), "Array should be frozen");
		});

		it("should contain a substantial number of ranges", () => {
			assert.ok(ranges.length > 0, "Should have at least one range");
			assert.ok(
				ranges.length > 100,
				`Expected >100 ranges for Unicode combining marks, got ${ranges.length}`,
			);
		});

		it("should have valid range pairs [start, end]", () => {
			for (const range of ranges) {
				assert.ok(
					Array.isArray(range),
					"Each entry should be an array",
				);
				assert.strictEqual(
					range.length,
					2,
					"Each entry should have exactly 2 elements",
				);

				const [start, end] = range;

				assert.ok(
					Number.isInteger(start),
					`Start should be an integer, got ${start}`,
				);
				assert.ok(
					Number.isInteger(end),
					`End should be an integer, got ${end}`,
				);
				assert.ok(
					start >= 0x80,
					`Start should be >= 0x80 (no ASCII combining marks), got 0x${start.toString(16)}`,
				);
				assert.ok(
					end <= 0x10ffff,
					`End should be a valid Unicode code point, got 0x${end.toString(16)}`,
				);
				assert.ok(
					start <= end,
					`Start (0x${start.toString(16)}) should be <= end (0x${end.toString(16)})`,
				);
			}
		});

		it("should have sorted, non-overlapping, non-adjacent ranges (maximally collapsed)", () => {
			for (let i = 1; i < ranges.length; i++) {
				const prevEnd = ranges[i - 1][1];
				const currStart = ranges[i][0];

				assert.ok(
					currStart > prevEnd + 1,
					`Ranges should be sorted, non-overlapping, and non-adjacent: ` +
						`range[${i - 1}] ends at 0x${prevEnd.toString(16)}, ` +
						`range[${i}] starts at 0x${currStart.toString(16)} ` +
						`(gap of ${currStart - prevEnd - 1})`,
				);
			}
		});
	});

	describe("coverage of known combining characters", () => {
		let ranges;

		before(() => {
			ranges = require(GENERATED_FILE);
		});

		// Mn (Mark, Nonspacing) - stable across Unicode versions
		[
			[0x0300, "COMBINING GRAVE ACCENT"],
			[0x0301, "COMBINING ACUTE ACCENT"],
			[0x0302, "COMBINING CIRCUMFLEX ACCENT"],
			[0x0303, "COMBINING TILDE"],
			[0x0308, "COMBINING DIAERESIS"],
			[0x036f, "COMBINING LATIN SMALL LETTER X"],
			[0x0591, "HEBREW ACCENT ETNAHTA"],
			[0x05bf, "HEBREW POINT RAFE"],
			[0x0670, "ARABIC LETTER SUPERSCRIPT ALEF"],
			[0x0711, "SYRIAC LETTER SUPERSCRIPT ALAPH"],
			[0x0e31, "THAI CHARACTER MAI HAN AKAT"],
			[0x0eb1, "LAO VOWEL SIGN AA"],
			[0x1ab0, "COMBINING DOUBLED CIRCUMFLEX ACCENT"],
			[0x1dc0, "COMBINING DOTTED GRAVE ACCENT"],
			[0x20d0, "COMBINING LEFT HARPOON ABOVE"],
			[0xfe20, "COMBINING LIGATURE LEFT HALF"],
		].forEach(([cp, name]) => {
			it(`should include Mn: ${name} (${formatCodePoint(cp)})`, () => {
				assert.ok(
					isInRanges(cp, ranges),
					`${name} (0x${cp.toString(16)}) should be in ranges`,
				);
			});
		});

		// Mc (Mark, Spacing Combining)
		[
			[0x0903, "DEVANAGARI SIGN VISARGA"],
			[0x093b, "DEVANAGARI VOWEL SIGN OOE"],
			[0x0982, "BENGALI SIGN ANUSVARA"],
			[0x0bbe, "TAMIL VOWEL SIGN AA"],
			[0x0d02, "MALAYALAM SIGN ANUSVARA"],
			[0x1031, "MYANMAR VOWEL SIGN E"],
		].forEach(([cp, name]) => {
			it(`should include Mc: ${name} (${formatCodePoint(cp)})`, () => {
				assert.ok(
					isInRanges(cp, ranges),
					`${name} (0x${cp.toString(16)}) should be in ranges`,
				);
			});
		});

		// Me (Mark, Enclosing)
		[
			[0x0488, "COMBINING CYRILLIC-OLD SLAVIC HUNDRED THOUSANDS"],
			[0x0489, "COMBINING CYRILLIC MILLIONS SIGN"],
			[0x20dd, "COMBINING ENCLOSING CIRCLE"],
			[0x20de, "COMBINING ENCLOSING SQUARE"],
			[0x20df, "COMBINING ENCLOSING DIAMOND"],
			[0x20e0, "COMBINING ENCLOSING CIRCLE BACKSLASH"],
		].forEach(([cp, name]) => {
			it(`should include Me: ${name} (${formatCodePoint(cp)})`, () => {
				assert.ok(
					isInRanges(cp, ranges),
					`${name} (0x${cp.toString(16)}) should be in ranges`,
				);
			});
		});
	});

	describe("exclusion of non-combining characters", () => {
		let ranges;

		before(() => {
			ranges = require(GENERATED_FILE);
		});

		[
			[0x0041, "LATIN CAPITAL LETTER A"],
			[0x0061, "LATIN SMALL LETTER A"],
			[0x0020, "SPACE"],
			[0x0030, "DIGIT ZERO"],
			[0x00c0, "LATIN CAPITAL LETTER A WITH GRAVE (precomposed)"],
			[0x00e9, "LATIN SMALL LETTER E WITH ACUTE (precomposed)"],
			[0x4e00, "CJK UNIFIED IDEOGRAPH-4E00"],
			[0x1f600, "GRINNING FACE (emoji)"],
			[0x1f1e6, "REGIONAL INDICATOR SYMBOL LETTER A"],
		].forEach(([cp, name]) => {
			it(`should exclude: ${name} (${formatCodePoint(cp)})`, () => {
				assert.ok(
					!isInRanges(cp, ranges),
					`${name} (0x${cp.toString(16)}) should NOT be in ranges`,
				);
			});
		});
	});

	/*
	 * These tests compare the generated ranges against Node.js's built-in
	 * \p{M} (Mark) regex, which matches exactly the Mn + Mc + Me categories.
	 *
	 * This catches staleness: if the generated file is outdated relative to
	 * the Unicode version in the current Node.js build, mismatches appear.
	 *
	 * These tests only run when full ICU is available (the common case in
	 * CI and development). On small-ICU builds, \p{M} is not available.
	 */
	(HAS_FULL_ICU ? describe : describe.skip)(
		"freshness: cross-reference with Node.js Unicode data",
		() => {
			let ranges;

			before(() => {
				ranges = require(GENERATED_FILE);
			});

			it("generated ranges should cover all BMP combining marks known to Node.js", () => {
				const combiningRegex = /\p{M}/u;
				const missingFromRanges = [];

				for (let cp = 0x0080; cp <= 0xffff; cp++) {
					// Skip surrogate range
					if (cp >= 0xd800 && cp <= 0xdfff) {
						continue;
					}

					const char = String.fromCodePoint(cp);
					const isNodeCombining = combiningRegex.test(char);
					const isRangeCombining = isInRanges(cp, ranges);

					if (isNodeCombining && !isRangeCombining) {
						missingFromRanges.push(cp);
					}
				}

				assert.deepStrictEqual(
					missingFromRanges.map(formatCodePoint),
					[],
					"All BMP combining marks from Node.js should be in generated ranges. " +
						"If this fails, re-run: node tools/generate-combining-ranges.js",
				);
			});

			it("generated ranges should closely match Node.js BMP combining marks", () => {
				/*
				 * The generated file is built from the Unicode Consortium's
				 * latest data, which may be newer than the ICU version bundled
				 * with the current Node.js build. Code points present in the
				 * generated file but not recognized by Node.js are acceptable
				 * (newer Unicode). A large number of extras may indicate a bug
				 * in the generator.
				 */
				const combiningRegex = /\p{M}/u;
				const extraInRanges = [];

				for (let cp = 0x0080; cp <= 0xffff; cp++) {
					if (cp >= 0xd800 && cp <= 0xdfff) {
						continue;
					}

					const char = String.fromCodePoint(cp);
					const isNodeCombining = combiningRegex.test(char);
					const isRangeCombining = isInRanges(cp, ranges);

					if (!isNodeCombining && isRangeCombining) {
						extraInRanges.push(cp);
					}
				}

				assert.ok(
					extraInRanges.length < 200,
					`Generated ranges contain ${extraInRanges.length} BMP code points ` +
						"not recognized as \\p{M} by this Node.js build. " +
						"A small number is expected due to Unicode version differences, " +
						"but a large number may indicate a generator bug. " +
						`First few: ${extraInRanges.slice(0, 10).map(formatCodePoint).join(", ")}`,
				);
			});

			it("generated ranges should cover supplementary-plane combining marks known to Node.js", () => {
				const combiningRegex = /\p{M}/u;

				/*
				 * Full scan of supplementary planes is slow. Instead, check
				 * around every supplementary range in the generated file, plus
				 * known supplementary combining mark blocks.
				 */
				const checkPoints = new Set();
				const supplementaryBlocks = [
					[0x10a01, 0x10a03],
					[0x11000, 0x11002],
					[0x11038, 0x11046],
					[0x1d165, 0x1d169],
					[0x1d16d, 0x1d172],
					[0x1d17b, 0x1d182],
					[0xe0100, 0xe01ef],
				];

				for (const [start, end] of supplementaryBlocks) {
					for (
						let cp = Math.max(start - 2, 0x10000);
						cp <= Math.min(end + 2, 0x10ffff);
						cp++
					) {
						checkPoints.add(cp);
					}
				}

				for (const [start, end] of ranges) {
					if (start > 0xffff) {
						for (
							let cp = Math.max(start - 2, 0x10000);
							cp <= Math.min(end + 2, 0x10ffff);
							cp++
						) {
							checkPoints.add(cp);
						}
					}
				}

				const missingFromRanges = [];

				for (const cp of checkPoints) {
					const char = String.fromCodePoint(cp);
					const isNodeCombining = combiningRegex.test(char);
					const isRangeCombining = isInRanges(cp, ranges);

					if (isNodeCombining && !isRangeCombining) {
						missingFromRanges.push(cp);
					}
				}

				assert.deepStrictEqual(
					missingFromRanges.map(formatCodePoint),
					[],
					"All supplementary combining marks from Node.js should be in generated ranges. " +
						"If this fails, re-run: node tools/generate-combining-ranges.js",
				);
			});
		},
	);

	describe("consistency with isCombiningCharacter module", () => {
		let ranges;
		let isCombiningCharacter;

		before(() => {
			ranges = require(GENERATED_FILE);
			isCombiningCharacter = require("../../lib/rules/utils/unicode/is-combining-character");
		});

		it("should agree with generated ranges for all range start/end points", () => {
			for (const [start, end] of ranges) {
				assert.strictEqual(
					isCombiningCharacter(start),
					true,
					`isCombiningCharacter(0x${start.toString(16)}) should be true (range start)`,
				);
				assert.strictEqual(
					isCombiningCharacter(end),
					true,
					`isCombiningCharacter(0x${end.toString(16)}) should be true (range end)`,
				);
			}
		});

		it("should return false for code points just outside ranges", () => {
			for (const [start, end] of ranges) {
				if (start > 0x80) {
					assert.strictEqual(
						isCombiningCharacter(start - 1),
						false,
						`isCombiningCharacter(0x${(start - 1).toString(16)}) should be false (before range)`,
					);
				}
				if (end < 0x10ffff) {
					assert.strictEqual(
						isCombiningCharacter(end + 1),
						false,
						`isCombiningCharacter(0x${(end + 1).toString(16)}) should be false (after range)`,
					);
				}
			}
		});

		it("should return false for all ASCII code points", () => {
			for (let cp = 0; cp <= 0x7f; cp++) {
				assert.strictEqual(
					isCombiningCharacter(cp),
					false,
					`isCombiningCharacter(0x${cp.toString(16)}) should be false (ASCII)`,
				);
			}
		});
	});
});
